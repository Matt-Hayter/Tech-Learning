//Iterating through a string:

const str = "Matthew"

for(let i = 0; i < str.length; i++) {
    console.log(str.charAt(i)) //Accessing a specific character
}
console.log("\n")

//Iterating through an array character strings:

const arr = ["M", "A", "T", "T"]

for(let i = 0; i < arr.length; i++) {
    console.log(arr[i]) //Accessing a specific element
}
